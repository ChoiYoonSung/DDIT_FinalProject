package com.spring.dto;

public class ScrapVO {

	private String scCode;
	private String empId;
	private String scTitle;
	private String scUrl;
	private String scRegdate;
	
	public String getScCode() {
		return scCode;
	}
	public void setScCode(String scCode) {
		this.scCode = scCode;
	}
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getScTitle() {
		return scTitle;
	}
	public void setScTitle(String scTitle) {
		this.scTitle = scTitle;
	}
	public String getScUrl() {
		return scUrl;
	}
	public void setScUrl(String scUrl) {
		this.scUrl = scUrl;
	}
	public String getScRegdate() {
		return scRegdate;
	}
	public void setScRegdate(String scRegdate) {
		this.scRegdate = scRegdate;
	}
	
	
	

}
