package com.spring.dto;

public class ReportVO {
	
	private String empId;
	private String bdCode;
	
	private String rpBd;
	private String rpTitle;
	private String rpCount;
	private String rpBdName;
	public String getRpBdName() {
		return rpBdName;
	}
	public void setRpBdName(String rpBdName) {
		this.rpBdName = rpBdName;
	}
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getBdCode() {
		return bdCode;
	}
	public void setBdCode(String bdCode) {
		this.bdCode = bdCode;
	}
	
	
	public String getRpBd() {
		return rpBd;
	}
	public void setRpBd(String rpBd) {
		this.rpBd = rpBd;
	}
	public String getRpTitle() {
		return rpTitle;
	}
	public void setRpTitle(String rpTitle) {
		this.rpTitle = rpTitle;
	}
	public String getRpCount() {
		return rpCount;
	}
	public void setRpCount(String rpCount) {
		this.rpCount = rpCount;
	}
	
	

}
