package com.spring.dto;

public class ProLogVO {
	private String plNo;
	private String plContent;
	private String plRegdate;
	
	
	public String getPlNo() {
		return plNo;
	}
	public void setPlNo(String plNo) {
		this.plNo = plNo;
	}
	public String getPlContent() {
		return plContent;
	}
	public void setPlContent(String plContent) {
		this.plContent = plContent;
	}
	public String getPlRegdate() {
		return plRegdate;
	}
	public void setPlRegdate(String plRegdate) {
		this.plRegdate = plRegdate;
	}
	
}
