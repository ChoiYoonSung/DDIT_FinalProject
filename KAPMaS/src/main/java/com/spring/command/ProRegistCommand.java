package com.spring.command;

import com.spring.dto.ProVO;

public class ProRegistCommand {

	private String pName     ;
	private String pTitle    ;
	private String pSummary  ;
	private String pContent  ;
	private String pStartdate;
	private String pEnddate  ;
	
	
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public String getpTitle() {
		return pTitle;
	}
	public void setpTitle(String pTitle) {
		this.pTitle = pTitle;
	}
	public String getpSummary() {
		return pSummary;
	}
	public void setpSummary(String pSummary) {
		this.pSummary = pSummary;
	}
	public String getpContent() {
		return pContent;
	}
	public void setpContent(String pContent) {
		this.pContent = pContent;
	}
	public String getpStartdate() {
		return pStartdate;
	}
	public void setpStartdate(String pStartdate) {
		this.pStartdate = pStartdate;
	}
	public String getpEnddate() {
		return pEnddate;
	}
	public void setpEnddate(String pEnddate) {
		this.pEnddate = pEnddate;
	}
	
	
	public ProVO toProVO() {
		ProVO pro = new ProVO();
		
		pro.setpName(pName);
		pro.setpTitle(pTitle);
		pro.setpSummary(pSummary);
		pro.setpContent(pContent);
		pro.setpStartdate(pStartdate);
		pro.setpEnddate(pEnddate);
		
		return pro;
	}
	
	
	
	
	
	
	
}
